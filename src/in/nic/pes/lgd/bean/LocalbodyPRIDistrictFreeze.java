package in.nic.pes.lgd.bean;

/*
 * Bean to get data for district pri freeze check function
 * 
 * */
public class LocalbodyPRIDistrictFreeze {
	
	private Integer lblc;
	private String lbName;
	private String lbStatus;
	/**
	 * @return the lblc
	 */
	public Integer getLblc() {
		return lblc;
	}
	/**
	 * @param lblc the lblc to set
	 */
	public void setLblc(Integer lblc) {
		this.lblc = lblc;
	}
	/**
	 * @return the lbName
	 */
	public String getLbName() {
		return lbName;
	}
	/**
	 * @param lbName the lbName to set
	 */
	public void setLbName(String lbName) {
		this.lbName = lbName;
	}
	/**
	 * @return the lbStatus
	 */
	public String getLbStatus() {
		return lbStatus;
	}
	/**
	 * @param lbStatus the lbStatus to set
	 */
	public void setLbStatus(String lbStatus) {
		this.lbStatus = lbStatus;
	}
	

}
