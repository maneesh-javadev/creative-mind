package in.nic.pes.lgd.forms;

public class StatePanchayatSetupReportForm {

	private String stateName;
	private int stateId;
	
	private int numberofTiers;
	private String districtPanchayatName;
	private String interMPanchayatName;
	private String villagePanchayatName;
	
	public String getStateName() {
		return stateName;
	}
	public void setStateName(String stateName) {
		this.stateName = stateName;
	}
	public int getStateId() {
		return stateId;
	}
	public void setStateId(int stateId) {
		this.stateId = stateId;
	}
	public int getNumberofTiers() {
		return numberofTiers;
	}
	public void setNumberofTiers(int numberofTiers) {
		this.numberofTiers = numberofTiers;
	}
	public String getDistrictPanchayatName() {
		return districtPanchayatName;
	}
	public void setDistrictPanchayatName(String districtPanchayatName) {
		this.districtPanchayatName = districtPanchayatName;
	}
	public String getInterMPanchayatName() {
		return interMPanchayatName;
	}
	public void setInterMPanchayatName(String interMPanchayatName) {
		this.interMPanchayatName = interMPanchayatName;
	}
	public String getVillagePanchayatName() {
		return villagePanchayatName;
	}
	public void setVillagePanchayatName(String villagePanchayatName) {
		this.villagePanchayatName = villagePanchayatName;
	}
	
	
}
